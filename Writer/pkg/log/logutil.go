package log

import (
	"fmt"
	"runtime"
	"strings"

	"github.com/Sirupsen/logrus"
)

const (
	// DebugLevel is the level to print debug logs
	DebugLevel logrus.Level = logrus.DebugLevel
)

var logger = logrus.New()

// Fields wraps logrus.Fields, which is a map[string]interface{}
type Fields logrus.Fields

// SetLogLevel sets the level of log
func SetLogLevel(level logrus.Level) {
	logger.Level = level
}

// SetLogFormatter sets the formatter of log
func SetLogFormatter(formatter logrus.Formatter) {
	logger.Formatter = formatter
}

// Debug logs a message at level Debug on the standard logger.
func Debug(args ...interface{}) {
	if logger.Level >= logrus.DebugLevel {
		entry := logger.WithFields(logrus.Fields{})
		entry.Data["_file_"] = fileInfo(2)
		entry.Debug(args)
	}
}

// Debugf logs a message at level Debug on the standard logger.
func Debugf(format string, args ...interface{}) {
	if logger.Level >= logrus.DebugLevel {
		entry := logger.WithFields(logrus.Fields{})
		entry.Data["_file_"] = fileInfo(2)
		entry.Debug(format, args)
	}
}

// DebugWithFields logs a message with fields at level Debug on the standard logger.
func DebugWithFields(l interface{}, f Fields) {
	if logger.Level >= logrus.DebugLevel {
		entry := logger.WithFields(logrus.Fields(f))
		entry.Data["_file_"] = fileInfo(2)
		entry.Debug(l)
	}
}

